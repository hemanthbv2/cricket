  function generatePointsTable() {
    const teams = Data().getTeams();
    const matches = Data().getMatches().filter(m => m.status === 'completed');
    const tournament = Data().getTournament();

    // Build standings
    const standings = {};
    teams.forEach(t => {
      standings[t.id] = { team: t, played: 0, won: 0, lost: 0, tied: 0, nr: 0, points: 0, nrr: 0, runsFor: 0, ballsFor: 0, runsAgainst: 0, ballsAgainst: 0 };
    });

    const pts = tournament.pointsSystem || { win: 2, loss: 0, tie: 1, nr: 1 };

    matches.forEach(m => {
      const s1 = standings[m.team1Id];
      const s2 = standings[m.team2Id];
      if (!s1 || !s2) return;
      s1.played++;
      s2.played++;

      if (m.innings) {
        m.innings.forEach(inn => {
          const s = standings[inn.battingTeamId];
          const opp = inn.battingTeamId === m.team1Id ? standings[m.team2Id] : standings[m.team1Id];
          if (s) { s.runsFor += inn.totalRuns || 0; s.ballsFor += inn.totalBalls || 0; }
          if (opp) { opp.runsAgainst += inn.totalRuns || 0; opp.ballsAgainst += inn.totalBalls || 0; }
        });
      }

      const result = (m.result || '').toLowerCase();
      if (m.status === 'no-result' || result.includes('no result')) {
        s1.nr++; s2.nr++; s1.points += pts.nr; s2.points += pts.nr;
      } else if (m.status === 'tied' || result.includes('tie')) {
        s1.tied++; s2.tied++; s1.points += pts.tie; s2.points += pts.tie;
      } else {
        const t1 = Data().getTeam(m.team1Id);
        const t2 = Data().getTeam(m.team2Id);
        if (m.result && t1 && m.result.includes(t1.name)) {
          s1.won++; s2.lost++; s1.points += pts.win; s2.points += pts.loss;
        } else if (m.result && t2 && m.result.includes(t2.name)) {
          s2.won++; s1.lost++; s2.points += pts.win; s1.points += pts.loss;
        }
      }
    });

    // Calculate NRR
    Object.values(standings).forEach(s => {
      const forRate = s.ballsFor > 0 ? (s.runsFor / s.ballsFor) * 6 : 0;
      const againstRate = s.ballsAgainst > 0 ? (s.runsAgainst / s.ballsAgainst) * 6 : 0;
      s.nrr = (forRate - againstRate).toFixed(3);
    });

    const sorted = Object.values(standings).sort((a, b) => b.points - a.points || parseFloat(b.nrr) - parseFloat(a.nrr));

    // Draw canvas
    const canvas = document.createElement('canvas');
    const W = 1200, H = 630;
    canvas.width = W;
    canvas.height = H;
    const ctx = canvas.getContext('2d');

    drawDarkBg(ctx, W, H);

    // Accent line
    const accentGrad = ctx.createLinearGradient(0, 0, W, 0);
    accentGrad.addColorStop(0, '#10b981');
    accentGrad.addColorStop(1, '#059669');
    ctx.fillStyle = accentGrad;
    ctx.fillRect(0, 0, W, 4);

    // Title
    ctx.fillStyle = '#f9fafb';
    ctx.font = '700 28px Outfit, sans-serif';
    ctx.textAlign = 'center';
    ctx.fillText(tournament.name || 'CrickDesk Tournament', W / 2, 50);
    ctx.fillStyle = '#9ca3af';
    ctx.font = '400 14px Inter, sans-serif';
    ctx.fillText('Points Table', W / 2, 74);

    // Table header
    const startY = 100;
    const rowH = 48;
    const cols = [
      { label: '#', x: 40, align: 'center' },
      { label: 'Team', x: 120, align: 'left' },
      { label: 'P', x: 420, align: 'center' },
      { label: 'W', x: 500, align: 'center' },
      { label: 'L', x: 570, align: 'center' },
      { label: 'T', x: 640, align: 'center' },
      { label: 'NR', x: 710, align: 'center' },
      { label: 'NRR', x: 810, align: 'center' },
      { label: 'Pts', x: 950, align: 'center' }
    ];

    // Header bg
    ctx.fillStyle = 'rgba(255,255,255,0.05)';
    ctx.fillRect(20, startY, W - 40, rowH);

    ctx.font = '600 13px Inter, sans-serif';
    ctx.fillStyle = '#9ca3af';
    cols.forEach(c => {
      ctx.textAlign = c.align;
      ctx.fillText(c.label, c.x, startY + 30);
    });

    // Rows
    sorted.forEach((s, i) => {
      const y = startY + rowH + i * rowH;

      if (i % 2 === 0) {
        ctx.fillStyle = 'rgba(255,255,255,0.02)';
        ctx.fillRect(20, y, W - 40, rowH);
      }

      // Team color bar
      ctx.fillStyle = s.team.color || '#10b981';
      ctx.fillRect(20, y, 4, rowH);

      ctx.font = '500 14px Inter, sans-serif';
      ctx.fillStyle = '#f9fafb';

      ctx.textAlign = 'center';
      ctx.fillText(i + 1, cols[0].x, y + 30);

      ctx.textAlign = 'left';
      ctx.font = '600 14px Inter, sans-serif';
      ctx.fillText(s.team.name, cols[1].x, y + 30);

      ctx.font = '400 14px Inter, sans-serif';
      ctx.textAlign = 'center';
      ctx.fillText(s.played, cols[2].x, y + 30);
      ctx.fillStyle = '#10b981';
      ctx.fillText(s.won, cols[3].x, y + 30);
      ctx.fillStyle = '#ef4444';
      ctx.fillText(s.lost, cols[4].x, y + 30);
      ctx.fillStyle = '#f9fafb';
      ctx.fillText(s.tied, cols[5].x, y + 30);
      ctx.fillText(s.nr, cols[6].x, y + 30);

      ctx.fillStyle = parseFloat(s.nrr) >= 0 ? '#10b981' : '#ef4444';
      ctx.fillText(s.nrr > 0 ? '+' + s.nrr : s.nrr, cols[7].x, y + 30);

      ctx.fillStyle = '#fbbf24';
      ctx.font = '700 16px Inter, sans-serif';
      ctx.fillText(s.points, cols[8].x, y + 30);
    });

    drawBrandFooter(ctx, W, H);

    // Preview
    const preview = document.getElementById('share-preview-points');
    if (preview) {
      preview.innerHTML = '';
      const img = document.createElement('img');
      img.src = canvas.toDataURL();
      img.style.cssText = 'width:100%;border-radius:8px;';
      preview.appendChild(img);
    }

    return canvas;
  }

