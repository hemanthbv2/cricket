  function exportCSV() {
    const matches = Data().getMatches().filter(m => m.status === 'completed');
    const players = Data().getPlayers();
    const map = {};

    players.forEach(p => {
      map[p.id] = {
        name: p.name,
        team: (Data().getTeam(p.teamId) || {}).name || '',
        role: p.role,
        matches: 0,
        runs: 0,
        balls: 0,
        fours: 0,
        sixes: 0,
        wickets: 0,
        bowlRuns: 0,
        bowlBalls: 0,
        catches: 0,
        matchSet: new Set()
      };
    });

    matches.forEach(m => {
      if (!m.innings) return;
      m.innings.forEach(inn => {
        if (inn.battingScorecard) {
          inn.battingScorecard.forEach(e => {
            const s = map[e.playerId];
            if (!s) return;
            if (!s.matchSet.has(m.id)) { s.matchSet.add(m.id); s.matches++; }
            s.runs += e.runs || 0;
            s.balls += e.balls || 0;
            s.fours += e.fours || 0;
            s.sixes += e.sixes || 0;
          });
        }
        if (inn.bowlingScorecard) {
          inn.bowlingScorecard.forEach(e => {
            const s = map[e.playerId];
            if (!s) return;
            if (!s.matchSet.has(m.id)) { s.matchSet.add(m.id); s.matches++; }
            s.wickets += e.wickets || 0;
            s.bowlRuns += e.runs || 0;
            const bpo = Data().getTournament().ballsPerOver || 6;
            s.bowlBalls += e.overs ? Math.floor(e.overs) * bpo + Math.round((e.overs % 1) * 10) : (e.balls || 0);
          });
        }
        if (inn.balls) {
          inn.balls.forEach(ball => {
            if (ball.wicket && ball.wicket.type === 'caught' && ball.wicket.fielderId && map[ball.wicket.fielderId]) {
              map[ball.wicket.fielderId].catches++;
            }
          });
        }
      });
    });

    const bpo = Data().getTournament().ballsPerOver || 6;
    let csv = 'Name,Team,Role,Matches,Runs,Balls,SR,4s,6s,Wickets,Bowl Runs,Bowl Overs,Econ,Catches\n';
    Object.values(map).filter(s => s.matches > 0).forEach(s => {
      const sr = s.balls > 0 ? ((s.runs / s.balls) * 100).toFixed(1) : '0.0';
      const overs = Utils().formatOvers(s.bowlBalls, bpo);
      const econ = Utils().calculateEconomy(s.bowlRuns, s.bowlBalls, bpo);
      csv += `"${s.name}","${s.team}","${s.role}",${s.matches},${s.runs},${s.balls},${sr},${s.fours},${s.sixes},${s.wickets},${s.bowlRuns},"${overs}",${econ},${s.catches}\n`;
    });

    const blob = new Blob([csv], { type: 'text/csv' });
    const link = document.createElement('a');
    link.download = 'crickdesk_stats.csv';
    link.href = URL.createObjectURL(blob);
    link.click();
    URL.revokeObjectURL(link.href);
    Utils().showToast('Stats exported as CSV!', 'success');
  }

