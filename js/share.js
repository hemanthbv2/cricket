      ctx.fillStyle = '#6b7280';
      ctx.font = '400 12px Inter, sans-serif';
      ctx.fillText('vs', x + 200, y + cardH / 2 - 2);

      ctx.beginPath();
      ctx.arc(x + 230, y + cardH / 2, 6, 0, Math.PI * 2);
      ctx.fillStyle = t2 ? t2.color : '#3b82f6';
      ctx.fill();

      ctx.fillStyle = '#f9fafb';
      ctx.font = '600 13px Inter, sans-serif';
      ctx.fillText(t2 ? t2.name : 'TBD', x + 244, y + cardH / 2 - 2);

