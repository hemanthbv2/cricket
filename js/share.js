  function generateTeamSquad(teamId) {
    const team = Data().getTeam(teamId);
    if (!team) return null;
    const players = Data().getPlayersByTeam(teamId);
    const tournament = Data().getTournament();

    const canvas = document.createElement('canvas');
    const W = 600, H = 800;
    canvas.width = W;
    canvas.height = H;
    const ctx = canvas.getContext('2d');

    drawDarkBg(ctx, W, H);

    // Team color header
    const headerH = 140;
    const headerGrad = ctx.createLinearGradient(0, 0, W, headerH);
    headerGrad.addColorStop(0, team.color || '#10b981');
    headerGrad.addColorStop(1, 'rgba(0,0,0,0.3)');
    ctx.fillStyle = headerGrad;
    ctx.fillRect(0, 0, W, headerH);

    // Team avatar
    ctx.beginPath();
    ctx.arc(W / 2, 55, 30, 0, Math.PI * 2);
    ctx.fillStyle = 'rgba(255,255,255,0.2)';
    ctx.fill();
    ctx.fillStyle = '#fff';
    ctx.font = '700 20px Outfit, sans-serif';
    ctx.textAlign = 'center';
    ctx.textBaseline = 'middle';
    ctx.fillText((team.shortName || team.name.substring(0, 3)).toUpperCase(), W / 2, 55);
    ctx.textBaseline = 'alphabetic';

    ctx.fillStyle = '#fff';
    ctx.font = '700 24px Outfit, sans-serif';
    ctx.fillText(team.name, W / 2, 115);

    // Tournament name
    ctx.fillStyle = 'rgba(255,255,255,0.6)';
    ctx.font = '400 12px Inter, sans-serif';
    ctx.fillText(tournament.name || 'CrickDesk Tournament', W / 2, 135);

    // Player list
    const startY = 170;
    const rowH = 50;

    const roleOrder = { 'wicket-keeper': 0, 'batsman': 1, 'all-rounder': 2, 'bowler': 3 };
    const sortedPlayers = [...players].sort((a, b) => {
      if (a.id === team.captainId) return -1;
      if (b.id === team.captainId) return 1;
      return (roleOrder[a.role] || 4) - (roleOrder[b.role] || 4);
    });

    sortedPlayers.forEach((p, i) => {
      const y = startY + i * rowH;
      const isCaptain = p.id === team.captainId;

      if (i % 2 === 0) {
        ctx.fillStyle = 'rgba(255,255,255,0.02)';
        ctx.fillRect(20, y, W - 40, rowH);
      }

      // Index
      ctx.fillStyle = '#6b7280';
      ctx.font = '400 12px Inter, sans-serif';
      ctx.textAlign = 'center';
      ctx.fillText(i + 1, 45, y + 30);

      // Name
      ctx.fillStyle = isCaptain ? '#fbbf24' : '#f9fafb';
      ctx.font = (isCaptain ? '700' : '500') + ' 14px Inter, sans-serif';
      ctx.textAlign = 'left';
      ctx.fillText(p.name + (isCaptain ? ' (C)' : ''), 75, y + 28);

      // Role badge
      const roleColors = { 'batsman': '#10b981', 'bowler': '#ef4444', 'all-rounder': '#8b5cf6', 'wicket-keeper': '#3b82f6' };
      const roleLabels = { 'batsman': 'BAT', 'bowler': 'BOWL', 'all-rounder': 'AR', 'wicket-keeper': 'WK' };
      const rc = roleColors[p.role] || '#6b7280';
      const rl = roleLabels[p.role] || p.role;

      roundRect(ctx, W - 90, y + 15, 55, 22, 6);
      ctx.fillStyle = rc + '33';
      ctx.fill();
      ctx.fillStyle = rc;
      ctx.font = '600 10px Inter, sans-serif';
      ctx.textAlign = 'center';
      ctx.fillText(rl, W - 62, y + 30);

      // Batting style
      ctx.fillStyle = '#6b7280';
      ctx.font = '400 11px Inter, sans-serif';
      ctx.textAlign = 'left';
      ctx.fillText(p.battingStyle === 'left' ? 'Left-hand' : 'Right-hand', 75, y + 44);
    });

    drawBrandFooter(ctx, W, H);

    const preview = document.getElementById('share-preview-squad');
    if (preview) {
      preview.innerHTML = '';
      const img = document.createElement('img');
      img.src = canvas.toDataURL();
      img.style.cssText = 'width:100%;border-radius:8px;';
      preview.appendChild(img);
    }

    return canvas;
  }

