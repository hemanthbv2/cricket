  //  ATTACH EVENTS
  // ══════════════════════════════════════════════════════════════════════

  function attachEvents() {
    const container = document.getElementById('page-share');
    if (!container || container.dataset.eventsAttached) return;
    container.dataset.eventsAttached = 'true';

    // Store generated canvases
    const canvases = {};

    container.addEventListener('click', e => {
      const target = e.target;

      // Points Table
      if (target.id === 'share-gen-points') {
        canvases.points = generatePointsTable();
        const dlBtn = document.getElementById('share-dl-points');
        if (dlBtn && canvases.points) dlBtn.disabled = false;
        Utils().showToast('Points table generated!', 'success');
      }
      if (target.id === 'share-dl-points' && canvases.points) {
        downloadCanvas(canvases.points, 'crickdesk_points_table.png');
      }

      // Match Summary
      if (target.id === 'share-gen-match') {
        const matchId = document.getElementById('share-match-select').value;
        if (!matchId) { Utils().showToast('Please select a match', 'error'); return; }
        canvases.match = generateMatchSummary(matchId);
        const dlBtn = document.getElementById('share-dl-match');
        if (dlBtn && canvases.match) dlBtn.disabled = false;
        Utils().showToast('Match summary generated!', 'success');
      }
      if (target.id === 'share-dl-match' && canvases.match) {
        downloadCanvas(canvases.match, 'crickdesk_match_summary.png');
      }

      // Fixtures
      if (target.id === 'share-gen-fixtures') {
        canvases.fixtures = generateFixtures();
        const dlBtn = document.getElementById('share-dl-fixtures');
        if (dlBtn && canvases.fixtures) dlBtn.disabled = false;
        Utils().showToast('Fixtures image generated!', 'success');
      }
      if (target.id === 'share-dl-fixtures' && canvases.fixtures) {
        downloadCanvas(canvases.fixtures, 'crickdesk_fixtures.png');
      }

      // Team Squad
      if (target.id === 'share-gen-squad') {
        const teamId = document.getElementById('share-team-select').value;
        if (!teamId) { Utils().showToast('Please select a team', 'error'); return; }
        canvases.squad = generateTeamSquad(teamId);
        const dlBtn = document.getElementById('share-dl-squad');
        if (dlBtn && canvases.squad) dlBtn.disabled = false;
        Utils().showToast('Team squad card generated!', 'success');
      }
      if (target.id === 'share-dl-squad' && canvases.squad) {
        downloadCanvas(canvases.squad, 'crickdesk_team_squad.png');
      }

      // Player Profile
      if (target.id === 'share-gen-player') {
        const playerId = document.getElementById('share-player-select').value;
        if (!playerId) { Utils().showToast('Please select a player', 'error'); return; }
        canvases.player = generatePlayerProfile(playerId);
        const dlBtn = document.getElementById('share-dl-player');
        if (dlBtn && canvases.player) dlBtn.disabled = false;
        Utils().showToast('Player profile card generated!', 'success');
      }
      if (target.id === 'share-dl-player' && canvases.player) {
        downloadCanvas(canvases.player, 'crickdesk_player_profile.png');
      }

      // Export JSON
      if (target.id === 'share-export-json') {
        exportJSON();
      }

      // Import
      if (target.id === 'share-import-btn') {
        const fileInput = document.getElementById('share-import-file');
        if (fileInput) fileInput.click();
      }

      // Export CSV
      if (target.id === 'share-export-csv') {
        exportCSV();
      }

      // Match Report
      if (target.id === 'share-gen-report') {
        const matchId = document.getElementById('share-report-match').value;
        if (!matchId) { Utils().showToast('Please select a match', 'error'); return; }
        generateMatchReport(matchId);
      }
    });

    container.addEventListener('change', e => {
      if (e.target.id === 'share-import-file') {
        const file = e.target.files[0];
        if (file) {
          Utils().confirmDialog('Importing data will overwrite ALL existing data. Are you sure?').then(ok => {
            if (ok) importJSON(file);
          });
        }
      }
    });
  }

