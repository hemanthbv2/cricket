  function generateMatchSummary(matchId) {
    const match = Data().getMatch(matchId);
    if (!match) return null;
    const t1 = Data().getTeam(match.team1Id);
    const t2 = Data().getTeam(match.team2Id);
    const tournament = Data().getTournament();
    const bpo = tournament.ballsPerOver || 6;

    const canvas = document.createElement('canvas');
    const W = 1200, H = 630;
    canvas.width = W;
    canvas.height = H;
    const ctx = canvas.getContext('2d');

    drawDarkBg(ctx, W, H);

    // Top accent
    const accentGrad = ctx.createLinearGradient(0, 0, W, 0);
    accentGrad.addColorStop(0, t1 ? t1.color : '#10b981');
    accentGrad.addColorStop(1, t2 ? t2.color : '#3b82f6');
    ctx.fillStyle = accentGrad;
    ctx.fillRect(0, 0, W, 5);

    // Tournament name
    ctx.fillStyle = '#9ca3af';
    ctx.font = '400 13px Inter, sans-serif';
    ctx.textAlign = 'center';
    ctx.fillText(tournament.name || 'CrickDesk Tournament', W / 2, 35);

    // Team avatars + names
    const drawTeamAvatar = (x, y, team) => {
      ctx.beginPath();
      ctx.arc(x, y, 35, 0, Math.PI * 2);
      ctx.fillStyle = team ? team.color : '#374151';
      ctx.fill();
      ctx.fillStyle = '#fff';
      ctx.font = '700 18px Outfit, sans-serif';
      ctx.textAlign = 'center';
      ctx.textBaseline = 'middle';
      const initials = team ? (team.shortName || team.name.substring(0, 2)).toUpperCase() : '??';
      ctx.fillText(initials, x, y);
      ctx.textBaseline = 'alphabetic';
    };

    drawTeamAvatar(300, 110, t1);
    ctx.fillStyle = '#f9fafb';
    ctx.font = '700 22px Outfit, sans-serif';
    ctx.textAlign = 'center';
    ctx.fillText(t1 ? t1.name : 'Team 1', 300, 165);

    // VS
    ctx.fillStyle = '#6b7280';
    ctx.font = '700 20px Outfit, sans-serif';
    ctx.fillText('VS', W / 2, 115);

    drawTeamAvatar(900, 110, t2);
    ctx.fillStyle = '#f9fafb';
    ctx.font = '700 22px Outfit, sans-serif';
    ctx.fillText(t2 ? t2.name : 'Team 2', 900, 165);

    // Scores
    let y = 210;
    if (match.innings) {
      match.innings.forEach((inn, idx) => {
        const team = Data().getTeam(inn.battingTeamId);
        const overs = Utils().formatOvers(inn.totalBalls || 0, bpo);
        const scoreStr = `${inn.totalRuns || 0}/${inn.totalWickets || 0} (${overs} ov)`;
        const x = inn.battingTeamId === match.team1Id ? 300 : 900;
        ctx.fillStyle = team ? team.color : '#10b981';
        ctx.font = '700 32px Outfit, sans-serif';
        ctx.textAlign = 'center';
        ctx.fillText(scoreStr, x, y);
      });
    }

    // Result
    ctx.fillStyle = '#fbbf24';
    ctx.font = '600 18px Inter, sans-serif';
    ctx.textAlign = 'center';
    ctx.fillText(match.result || '', W / 2, 280);

    // Divider
    ctx.strokeStyle = 'rgba(255,255,255,0.08)';
    ctx.beginPath();
    ctx.moveTo(60, 310);
    ctx.lineTo(W - 60, 310);
    ctx.stroke();

    // Key stats from innings
    let statsY = 350;
    ctx.font = '500 14px Inter, sans-serif';

    if (match.innings) {
      match.innings.forEach(inn => {
        const team = Data().getTeam(inn.battingTeamId);
        const x = inn.battingTeamId === match.team1Id ? 300 : 900;

        if (inn.battingScorecard && inn.battingScorecard.length > 0) {
          const topBat = [...inn.battingScorecard].sort((a, b) => (b.runs || 0) - (a.runs || 0))[0];
          if (topBat) {
            const p = Data().getPlayer(topBat.playerId);
            ctx.fillStyle = '#f9fafb';
            ctx.textAlign = 'center';
            ctx.fillText(`🏏 ${p ? p.name : 'Unknown'}: ${topBat.runs}(${topBat.balls})`, x, statsY);
          }
        }
        if (inn.bowlingScorecard && inn.bowlingScorecard.length > 0) {
          const topBowl = [...inn.bowlingScorecard].sort((a, b) => (b.wickets || 0) - (a.wickets || 0))[0];
          if (topBowl) {
            const p = Data().getPlayer(topBowl.playerId);
            ctx.fillStyle = '#f9fafb';
            ctx.fillText(`🎯 ${p ? p.name : 'Unknown'}: ${topBowl.wickets}/${topBowl.runs}`, x, statsY + 28);
          }
        }
      });
    }

    // Man of the Match
    if (match.manOfMatch) {
      const mom = Data().getPlayer(match.manOfMatch);
      if (mom) {
        ctx.fillStyle = '#fbbf24';
        ctx.font = '600 16px Inter, sans-serif';
        ctx.textAlign = 'center';
        ctx.fillText(`⭐ Man of the Match: ${mom.name}`, W / 2, 460);
      }
    }

    // Date & Venue
    ctx.fillStyle = '#6b7280';
    ctx.font = '400 13px Inter, sans-serif';
    ctx.textAlign = 'center';
    const dateStr = match.date ? Utils().formatDate(match.date) : '';
    const venueStr = match.venue || '';
    ctx.fillText(`${dateStr}${venueStr ? ' • ' + venueStr : ''}`, W / 2, 520);

    // Toss
    if (match.tossWonBy) {
      const tossTeam = Data().getTeam(match.tossWonBy);
      ctx.fillText(`Toss: ${tossTeam ? tossTeam.name : '?'} won, chose to ${match.tossDecision || '?'}`, W / 2, 545);
    }

    drawBrandFooter(ctx, W, H);

    const preview = document.getElementById('share-preview-match');
    if (preview) {
      preview.innerHTML = '';
      const img = document.createElement('img');
      img.src = canvas.toDataURL();
      img.style.cssText = 'width:100%;border-radius:8px;';
      preview.appendChild(img);
    }

    return canvas;
  }

